import { test, expect } from '@playwright/test'

/**
 * Сценарии Telegram не выполняются в браузере (клиент Telegram, long-polling бота).
 * Здесь только фиксация требований; проверка — вручную или интеграционными тестами бэкенда/бота.
 */
test.describe('Telegram-бот (спецификация, без браузерного e2e)', () => {
  test('задокументированные сценарии перечислены для регрессии вне этого прогона', () => {
    const scenarios = [
      'пользователь вводит /prognoz и дату, подписывается на ежедневные прогнозы',
      'второй /prognoz из того же chat не создаёт дубль подписки',
      'утром пользователь получает гороскоп по cron (08:00 МСК)',
      'после /stop рассылка прекращается (active=false)',
    ]
    expect(scenarios).toHaveLength(4)
  })
})
