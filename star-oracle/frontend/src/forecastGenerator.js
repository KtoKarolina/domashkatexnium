export function generateDailyForecast(birthDateStr, todayStr) {
  const sign = getZodiacSign(birthDateStr)
  const seed = simpleHash(birthDateStr + todayStr + sign.name)
  const daySeed = simpleHash(todayStr)

  const tithi = pick(TITHIS, daySeed)
  const nakshatra = pick(NAKSHATRAS, daySeed + 3)
  const quality = pick(NAKSHATRA_QUALITIES, daySeed + sign.idx)

  const vedicHint = `${sign.name} · Титхи: ${tithi} · Накшатра: ${nakshatra} · ${quality}`

  const elementTexts = MAIN_TEXTS[sign.element] || MAIN_TEXTS['Огонь']
  const mainText = pick(elementTexts, seed)

  const luckyNumber = ((seed % 9) + 1)
  const luckyNumberNote = `Число ${luckyNumber} сегодня ${pick(LUCKY_NOTES, seed + 5)}.`

  const favorable = pickN(FAVORABLE, seed + 1, 4)
  const avoid = pickN(AVOID, seed + 2, 4)

  return { vedicHint, mainText, luckyNumber, luckyNumberNote, favorable, avoid }
}
