function getZodiacSign(birthDate) {
  const d = new Date(birthDate + 'T12:00:00')
  const m = d.getMonth() + 1
  const day = d.getDate()

  for (let i = 0; i < SIGN_RANGES.length; i++) {
    const [sm, sd, em, ed] = SIGN_RANGES[i]
    if (sm <= em) {
      if ((m === sm && day >= sd) || (m === em && day <= ed) || (m > sm && m < em)) {
        return ZODIAC[i]
      }
    } else {
      if ((m === sm && day >= sd) || (m === em && day <= ed) || m > sm || m < em) {
        return ZODIAC[i]
      }
    }
  }
  return ZODIAC[9]
}

function simpleHash(str) {
  let h = 0
  for (let i = 0; i < str.length; i++) {
    h = ((h << 5) - h + str.charCodeAt(i)) | 0
  }
  return Math.abs(h)
}

function pick(arr, seed) {
  return arr[seed % arr.length]
}

function pickN(arr, seed, n) {
  const result = []
  const used = new Set()
  for (let i = 0; i < n && result.length < arr.length; i++) {
    let idx = (seed + i * 7 + i * i) % arr.length
    while (used.has(idx)) idx = (idx + 1) % arr.length
    used.add(idx)
    result.push(arr[idx])
  }
  return result
}

